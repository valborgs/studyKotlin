// 세미콜론 : 프로그래밍 언어들 중에 문장의 마지막임을 표시하기 위해 세미콜론(;)을 찍어줘야 하는 것들이 있다.

// 대다수의 개발자들은 세미콜론을 통해 명령문을 구분하여 한 줄에 여러 개의 명령문을 작성하지 않고 한 줄에 하나의 명령문을 작성하는 경우가 일반적이다.
// 이에, 일부 프로그래밍 언어들은 세미콜론을 선택 사항으로 제공한다.

fun main() {
    println("세미콜론 없음")
    println("세미콜론 있음");

    // 만약 한 줄에 여러 명령문을 작성하겠다면 각각을 세미콜론으로 구분해줘야 한다.
    println("명령문1"); println("명령문2"); println("명령문3");
}